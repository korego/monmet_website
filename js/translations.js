// Current language (default: English)
let currentLanguage = localStorage.getItem('language') || 'en';

// Function to switch language
function switchLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      // Check if element has data-i18n-html attribute for innerHTML
      if (element.hasAttribute('data-i18n-html')) {
        element.innerHTML = translations[lang][key];
      } 
      // Check if it's a placeholder
      else if (element.hasAttribute('placeholder')) {
        element.placeholder = translations[lang][key];
      }
      // Default to textContent
      else {
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update copyright year dynamically
  const currentYear = new Date().getFullYear();
  const copyrightElement = document.querySelector('[data-i18n="footer_copyright"]');
  if (copyrightElement && translations[lang]['footer_copyright']) {
    copyrightElement.textContent = translations[lang]['footer_copyright'].replace('{year}', currentYear);
  }
  
  // Update active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });
  
  // Update Read More/Less buttons in testimonials
  document.querySelectorAll('.read-more-text').forEach(btn => {
    const isExpanded = btn.closest('.read-more-btn').classList.contains('expanded');
    btn.textContent = translations[lang][isExpanded ? 'read_less' : 'read_more'];
  });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  switchLanguage(currentLanguage);
});
